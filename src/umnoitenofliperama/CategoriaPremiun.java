package umnoitenofliperama;

public class CategoriaPremiun {
    private String nome;
    private int quantidadeDeTiquetisTroca;
    private int quantidadeDePremiosDisponivel;

    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public int getQuantidadeDeTiquetisTroca() {
        return quantidadeDeTiquetisTroca;
    }
    public void setQuantidadeDeTiquetisTroca(int quantidadeDeTiquetisTroca) {
        this.quantidadeDeTiquetisTroca = quantidadeDeTiquetisTroca;
    }
    public int getQuantidadeDePremiosDisponivel() {
        return quantidadeDePremiosDisponivel;
    }
    
}
